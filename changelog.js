// Helper function to get changelog for specific version and language
export function getChangelog(version, language = 'en') {
    const versionKey = `v${version.replace(/\./g, '_')}`;
    const versionData = changelog[versionKey];
    
    if (!versionData) {
        console.error(`Changelog not found for version ${version}`);
        return { features: [], fixes: [] };
    }
    
    const langData = versionData[language] || versionData['en'];
    return langData;
}
